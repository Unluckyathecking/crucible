go_mod_path = os.path.join(GO_DIR, "go.mod")
go_module = "github.com/Unluckyathecking/crucible/clients/go"
go_version = "1.22"
if os.path.exists(go_mod_path):
    with open(go_mod_path) as _f:
        for _line in _f:
