        call = f'c.{fn}(context.Background(), "test-key", map[string]any{{"x": 1}})'
    elif op.has_body:
        call = f'c.{fn}(context.Background(), map[string]any{{"x": 1}})'
    elif op.secure:
        call = f'c.{fn}(context.Background(), "test-key")'
    else:
        call = f'c.{fn}(context.Background())'

