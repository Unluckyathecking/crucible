# ── Response types ────────────────────────────────────────────────────────────

def go_response_type(op):
    if op.no_content:
        return None
    schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
    props  = schema.get("properties", {})
    if props and not schema.get("additionalProperties"):
        return go_name(op.op_id) + "Response"
    return "map[string]any"

# array_item_properties returns the element row's `properties` dict for an
# array-typed field schema, or {} if it has none. Checks this codebase's
# openapi.go idiom of attaching `properties` directly to the array schema
# (non-standard JSON Schema, but what /v1/errors and /v1/keys actually use)
# as well as the standards-correct `items.properties` shape, so either
# documents a typed row instead of falling back to an untyped list.
def array_item_properties(fschema):
    if fschema.get("properties"):
        return fschema["properties"]
    items = fschema.get("items")
    if isinstance(items, dict) and items.get("properties"):
        return items["properties"]
    return {}

# array_item_required is array_item_properties' counterpart for the sibling
# `required` list on an array item's row schema (same two shapes: `required`
# attached directly to the array schema, or nested under `items`).
def array_item_required(fschema):
    if fschema.get("properties"):
        return fschema.get("required")
    items = fschema.get("items")
    if isinstance(items, dict) and items.get("properties"):
        return items.get("required")
    return None

def go_leaf_type(fschema):
    ftype = fschema.get("type", "any")
    add   = fschema.get("additionalProperties")
    if ftype == "string":
        return "string"
    elif ftype == "boolean":
        return "bool"
    elif ftype == "integer":
        return "int64"
    elif ftype == "number":
        return "float64"
    elif ftype == "array":
        return "[]any"
    elif ftype == "object" and add:
        inner    = add.get("type", "any")
        inner_go = {"string": "string", "integer": "int64", "number": "float64", "boolean": "bool"}.get(inner, "any")
        return f"map[string]{inner_go}"
    return "any"

def go_struct_field_block(props):
    return "\n".join(
        f'\t{fname.capitalize()} {go_leaf_type(fschema)} `json:"{fname}"`'
        for fname, fschema in sorted(props.items())
    )

def go_response_structs():
    parts = []
    for op in ops:
        rtype = go_response_type(op)
        if rtype in (None, "map[string]any"):
            continue
        schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
        props   = schema.get("properties", {})
        fields  = []
        for fname, fschema in sorted(props.items()):
            go_field = fname.capitalize()
            ftype = fschema.get("type", "any")
            if ftype == "array":
                item_props = array_item_properties(fschema)
                if item_props:
                    item_type = f"{rtype}{fname.capitalize()}Item"
                    parts.append(
                        f'// {item_type} is one row of {go_name(op.op_id)}\'s "{fname}" list.\n'
                        f"type {item_type} struct {{\n{go_struct_field_block(item_props)}\n}}"
                    )
                    go_type = f"[]{item_type}"
                else:
                    go_type = "[]any"
            else:
                go_type = go_leaf_type(fschema)
            fields.append(f'\t{go_field} {go_type} `json:"{fname}"`')
        field_block = "\n".join(fields)
        parts.append(
            f"// {rtype} is returned by {go_name(op.op_id)}.\n"
            f"type {rtype} struct {{\n{field_block}\n}}"
        )
    return "\n\n".join(parts) + ("\n\n" if parts else "")

# Any integer query parameter across all ops needs strconv for formatting;
# only import it when actually used so unused-import can never fail the build.
_needs_strconv = any(ptype == "integer" for op in ops for _, ptype in op.query_params)

def go_method_signature_params(op):
    params = ["ctx context.Context"]
    if op.secure:
        params.append("apiKey string")
    for p in op.path_params:
        params.append(f"{param_arg_name(p)} string")
    if op.has_body:
        params.append("payload any")
    for name, ptype in op.query_params:
        go_type = "int64" if ptype == "integer" else "string"
        params.append(f"{param_arg_name(name)} {go_type}")
    return ", ".join(params)

def go_path_and_query_lines(op):
    """Returns (lines, path_expr): lines build any local vars needed to resolve
    the request path (path-param substitution and/or a query string), and
    path_expr is the Go expression to pass as the request path."""
    lines = []
    path_var = None
    if op.path_params:
        fmt_path = re.sub(r"\{(\w+)\}", "%s", op.path)
        args = ", ".join(f"url.PathEscape({param_arg_name(p)})" for p in op.path_params)
        lines.append(f'\tpath := fmt.Sprintf("{fmt_path}", {args})')
        path_var = "path"

    if op.query_params:
        lines.append("\tq := url.Values{}")
        for name, ptype in op.query_params:
            arg = param_arg_name(name)
            if ptype == "integer":
                lines.append(f"\tif {arg} != 0 {{")
                lines.append(f'\t\tq.Set("{name}", strconv.FormatInt({arg}, 10))')
                lines.append("\t}")
            else:
                lines.append(f'\tif {arg} != "" {{')
                lines.append(f'\t\tq.Set("{name}", {arg})')
                lines.append("\t}")
        if path_var is None:
            lines.append(f'\treqPath := "{op.path}"')
            path_var = "reqPath"
        lines.append("\tif len(q) > 0 {")
        lines.append(f'\t\t{path_var} += "?" + q.Encode()')
        lines.append("\t}")

    path_expr = path_var if path_var is not None else f'"{op.path}"'
    return lines, path_expr

def go_method(op):
    fn    = go_name(op.op_id)
    rtype = go_response_type(op)
    method_const = {
        "get": "http.MethodGet", "post": "http.MethodPost",
        "delete": "http.MethodDelete", "patch": "http.MethodPatch",
    }[op.method]

    param_str = go_method_signature_params(op)
    path_lines, path_expr = go_path_and_query_lines(op)

    if rtype is None:
        ret_sig = "error"
        nil_ret = ""
    elif rtype == "map[string]any":
        ret_sig = "(map[string]any, error)"
        nil_ret = "nil, "
    else:
        ret_sig = f"(*{rtype}, error)"
        nil_ret = "nil, "

    lines = [f"// {fn} calls {op.method.upper()} {op.path} ({op.op_id.replace('_', ' ')})."]
    if op.secure:
        lines.append(f"// apiKey is sent as the {api_key_header} header.")
    lines.append(f"func (c *Client) {fn}({param_str}) {ret_sig} {{")
    if op.has_body and op.body_required:
        lines += [
            "\tbody, err := json.Marshal(payload)",
            "\tif err != nil {",
            f'\t\treturn {nil_ret}fmt.Errorf("crucible: marshal payload: %w", err)',
            "\t}",
        ]
    elif op.has_body:
        # requestBody is optional in the spec: payload == nil means "use
        # server-side defaults" (e.g. POST /v1/keys/{id}/rotate falls back to
        # a default grace period). It must NOT become an empty (zero-byte)
        # request body: some optional-body handlers decode with
        # json.Decoder.Decode and treat that io.EOF as a 400 (no special
        # case for "no body sent"), so this always sends a valid JSON body —
        # {} by default — never omits it outright.
        lines += [
            "\tif payload == nil {",
            "\t\tpayload = map[string]any{}",
            "\t}",
            "\tbody, err := json.Marshal(payload)",
            "\tif err != nil {",
            f'\t\treturn {nil_ret}fmt.Errorf("crucible: marshal payload: %w", err)',
            "\t}",
        ]
    lines += path_lines
    api_key_arg = "apiKey" if op.secure else '""'
    body_arg    = "body"   if op.has_body else "nil"
    lines += [
        f'\tresp, err := c.do(ctx, {method_const}, {path_expr}, {api_key_arg}, {body_arg})',
        "\tif err != nil {",
        f"\t\treturn {nil_ret}err",
        "\t}",
        "\tdefer resp.Body.Close()",
        "\tif err := checkError(resp); err != nil {",
        f"\t\treturn {nil_ret}err",
        "\t}",
    ]
    if rtype is None:
        lines.append("\treturn nil")
    elif rtype == "map[string]any":
        lines += [
            "\tout := make(map[string]any)",
            "\tif decErr := json.NewDecoder(resp.Body).Decode(&out); decErr != nil {",
            '\t\treturn nil, fmt.Errorf("crucible: decode response: %w", decErr)',
            "\t}",
            "\tif out == nil {",
            "\t\tout = make(map[string]any)",
            "\t}",
            "\treturn out, nil",
        ]
    else:
        lines += [
            f"\tvar out {rtype}",
            "\tif decErr := json.NewDecoder(resp.Body).Decode(&out); decErr != nil {",
            '\t\treturn nil, fmt.Errorf("crucible: decode response: %w", decErr)',
            "\t}",
            "\treturn &out, nil",
        ]
    lines.append("}")
    return "\n".join(lines)

structs_go = go_response_structs()
methods_go = "\n\n".join(go_method(op) for op in ops)

go_imports = ["bytes", "context", "encoding/json", "fmt", "io", "net/http", "net/url"]
if _needs_strconv:
    go_imports.append("strconv")
go_imports.append("strings")
go_imports_block = "\n".join(f'\t"{imp}"' for imp in go_imports)

