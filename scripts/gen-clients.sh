# ── TypeScript response types ─────────────────────────────────────────────────

def ts_response_type(op):
    if op.no_content:
        return "void"
    schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
    props  = schema.get("properties", {})
    if props and not schema.get("additionalProperties"):
        return go_name(op.op_id) + "Response"
    return "Record<string, unknown>"

# nullable_by_description flags a leaf field as nullable based on this
# codebase's openapi.go convention: every field whose wire value can actually
# be JSON null documents that in its Description ("null if unset", "null
# otherwise", "null means every event type", etc — audited against every
# Description in openapi.go, zero false positives). There's no structured
# schema-level nullability signal available (the Schema type has no such
# field, and per-item schemas built from a reused Properties map, like the
# /v1/keys and /v1/errors list rows, don't even carry a sibling "required"
# list to fall back on), so this is the only spec-derivable signal gen-
# clients.sh has for it.
def nullable_by_description(fschema):
    return "null" in fschema.get("description", "").lower()

def ts_leaf_type(fschema):
    ftype = fschema.get("type", "unknown")
    add   = fschema.get("additionalProperties")
    if ftype == "string":
        base = "string"
    elif ftype == "boolean":
        base = "boolean"
    elif ftype == "integer" or ftype == "number":
        base = "number"
    elif ftype == "object" and add:
        inner    = add.get("type", "unknown")
        inner_ts = {"string": "string", "integer": "number", "number": "number", "boolean": "boolean"}.get(inner, "unknown")
        base = f"Record<string, {inner_ts}>"
    else:
        base = "unknown"
    return f"{base} | null" if nullable_by_description(fschema) else base

def ts_interface_field_block(props):
    return "\n".join(f"  {fname}: {ts_leaf_type(fschema)};" for fname, fschema in sorted(props.items()))

def ts_response_interface(op):
    rtype = ts_response_type(op)
    if rtype in ("void", "Record<string, unknown>"):
        return ""
    schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
    props   = schema.get("properties", {})
    fields  = []
    nested_interfaces = []
    for fname, fschema in sorted(props.items()):
        ftype = fschema.get("type", "unknown")
        if ftype == "array":
            item_props = array_item_properties(fschema)
            if item_props:
                item_type = f"{rtype}{fname.capitalize()}Item"
                nested_interfaces.append(
                    f"export interface {item_type} {{\n{ts_interface_field_block(item_props)}\n}}\n"
                )
                ts_type = f"{item_type}[] | null"
            else:
                # The Go structs backing these responses use a bare slice
                # field with no `omitempty`, which encodes as JSON null when
                # nil (e.g. a webhook endpoint created without
                # subscribed_events); the OpenAPI schema has no way to mark
                # that nullability, so this generator assumes it for every
                # array field rather than promising callers a type array
                # methods can be called on unconditionally.
                ts_type = "unknown[] | null"
        else:
            ts_type = ts_leaf_type(fschema)
        fields.append(f"  {fname}: {ts_type};")
    iface = f"export interface {rtype} {{\n" + "\n".join(fields) + "\n}\n"
    return "".join(nested_interfaces) + iface

interfaces = "".join(iface for op in ops if (iface := ts_response_interface(op)))

def path_expr_ts(op):
    if not op.path_params:
        return f'"{op.path}"'
    tpl = op.path
    for p in op.path_params:
        tpl = tpl.replace("{" + p + "}", "${encodeURIComponent(" + param_arg_name(p) + ")}")
    return f"`{tpl}`"

def ts_method(op):
    fn    = ts_name(op.op_id)
    rtype = ts_response_type(op)

    param_parts = []
    for p in op.path_params:
        param_parts.append(f"{param_arg_name(p)}: string")
    if op.has_body and op.body_required:
        param_parts.append("payload: Record<string, unknown>")
    elif op.has_body:
        # requestBody is optional in the spec: omitting payload means "use
        # server-side defaults" (e.g. POST /v1/keys/{id}/rotate falls back
        # to a default grace period). The body sent is never fully omitted
        # though — see body_arg below.
        param_parts.append("payload?: Record<string, unknown>")
    for name, ptype in op.query_params:
        ts_type = "number" if ptype == "integer" else "string"
        param_parts.append(f"{param_arg_name(name)}?: {ts_type}")
    if op.secure:
        param_parts.append("apiKey?: string")
    params = ", ".join(param_parts)

    base_path_expr = path_expr_ts(op)
    body_lines = []
    if op.query_params:
        body_lines.append(f"    let path: string = {base_path_expr};")
        body_lines.append("    const q = new URLSearchParams();")
        for name, ptype in op.query_params:
            arg = param_arg_name(name)
            body_lines.append(f'    if ({arg} !== undefined) q.set("{name}", String({arg}));')
        body_lines.append('    if ([...q].length > 0) path += "?" + q.toString();')
        call_path_expr = "path"
    else:
        call_path_expr = base_path_expr

    method_upper = op.method.upper()
    if op.has_body and op.body_required:
        body_arg = "payload"
    elif op.has_body:
        # Some optional-body handlers decode with json.Decoder.Decode and
        # treat an empty (zero-byte) body's io.EOF as a 400 — there's no
        # universal "no body sent" case they all tolerate — so this always
        # sends a valid JSON body, {} by default, never omits it outright.
        body_arg = "payload ?? {}"
    else:
        body_arg = "undefined"
    api_key_expr = "apiKey ?? this.defaultApiKey" if op.secure else "undefined"

    doc_lines = [
        "  /**",
        f"   * {method_upper} {op.path} — {op.op_id.replace('_', ' ')}.",
    ]
    if op.secure:
        doc_lines.append("   * @param apiKey - Override the default API key for this call.")
    doc_lines.append("   */")
    doc_block = "\n".join(doc_lines)

    if rtype == "void":
        ret_type = "Promise<void>"
        call_line = f'    await this.requestVoid("{method_upper}", {call_path_expr}, {body_arg}, {api_key_expr});'
    else:
        ret_type = f"Promise<{rtype}>"
        call_line = f'    return this.request<{rtype}>("{method_upper}", {call_path_expr}, {body_arg}, {api_key_expr});'

    body_all = "\n".join(body_lines + [call_line])
    return (
        f"{doc_block}\n"
        f"  async {fn}({params}): {ret_type} {{\n"
        f"{body_all}\n"
        f"  }}"
    )

