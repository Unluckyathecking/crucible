{header}
// Package crucible is a type-safe Go client for the Crucible Gateway API.
// Construct a Client with New; all timeouts are the caller's responsibility.
package crucible

import (
{go_imports_block}
)

// Client calls the Crucible Gateway. Construct with New.
type Client struct {{
\tbaseURL *url.URL
\thttp    *http.Client
}}

// New returns a Client targeting baseURL, or an error if baseURL cannot be parsed.
// httpClient defaults to http.DefaultClient when nil. Note: http.DefaultClient has no
// timeout; callers must supply a client with Timeout set or use context.WithTimeout.
// baseURL is normalized: query strings, fragments, and credentials are stripped; trailing
// slashes on the path are removed so that appending spec paths (which start with "/") is correct.
// Any base path prefix (e.g. "/crucible" for a reverse-proxy subpath) is preserved.
func New(baseURL string, httpClient *http.Client) (*Client, error) {{
\tif httpClient == nil {{
\t\thttpClient = http.DefaultClient
\t}}
\tu, err := url.Parse(baseURL)
\tif err != nil {{
\t\treturn nil, fmt.Errorf("crucible: invalid baseURL %q: %w", baseURL, err)
\t}}
\tu.RawQuery = ""
\tu.Fragment = ""
\tu.User = nil
\tu.Path = strings.TrimRight(u.Path, "/")
\treturn &Client{{baseURL: u, http: httpClient}}, nil
}}

{structs_go}{methods_go}

func (c *Client) do(ctx context.Context, method, path, apiKey string, body []byte) (*http.Response, error) {{
\t// Append path directly: c.baseURL has no trailing slash; path starts with "/".
\t// This preserves any base path prefix (e.g. a reverse-proxy subpath).
\treqURL := c.baseURL.String() + path
\tvar req *http.Request
\tvar err error
\tif body != nil {{
\t\treq, err = http.NewRequestWithContext(ctx, method, reqURL, bytes.NewReader(body))
\t}} else {{
\t\treq, err = http.NewRequestWithContext(ctx, method, reqURL, nil)
\t}}
\tif err != nil {{
\t\treturn nil, fmt.Errorf("crucible: build request: %w", err)
\t}}
\tif body != nil {{
\t\treq.Header.Set("Content-Type", "application/json")
\t}}
\tif apiKey != "" {{
\t\treq.Header.Set("{api_key_header}", apiKey)
\t}}
\treq.Header.Set("Accept", "application/json")
\treturn c.http.Do(req)
}}

// checkError returns nil for 2xx responses without touching resp.Body.
// For non-2xx it reads the body (bounded to 64 KiB) to decode the gateway
// error envelope. The caller's defer resp.Body.Close() handles cleanup.
func checkError(resp *http.Response) error {{
\tif resp.StatusCode >= 200 && resp.StatusCode < 300 {{
\t\treturn nil
\t}}
\tvar envelope struct {{
\t\tError struct {{
\t\t\tCode      string `json:"code"`
\t\t\tMessage   string `json:"message"`
\t\t\tRetryable bool   `json:"retryable"`
\t\t\tRequestID string `json:"request_id"`
\t\t}} `json:"error"`
\t}}
\t// Read up to 64 KiB + 1; the extra byte lets us detect oversized bodies explicitly.
\tconst limit = 64 << 10
\tbody, readErr := io.ReadAll(io.LimitReader(resp.Body, limit+1))
\tif readErr != nil {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (read body: %v)", resp.StatusCode, readErr)}}
\t}}
\tif len(body) > limit {{
\t\t// Best-effort bounded drain; connection may not be reusable if body exceeds ~68 KiB.
\t\t_, _ = io.CopyN(io.Discard, resp.Body, 4<<10)
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (error body >%d bytes)", resp.StatusCode, limit)}}
\t}}
\tif decErr := json.Unmarshal(body, &envelope); decErr != nil {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (invalid JSON: %v)", resp.StatusCode, decErr)}}
\t}}
\tif envelope.Error.Code == "" {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (missing error code)", resp.StatusCode)}}
\t}}
\treturn &APIError{{
\t\tCode:      envelope.Error.Code,
\t\tMessage:   envelope.Error.Message,
\t\tRetryable: envelope.Error.Retryable,
\t\tRequestID: envelope.Error.RequestID,
\t}}
}}
""")

