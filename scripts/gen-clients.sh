# ── Response types ────────────────────────────────────────────────────────────

def go_response_type(op):
    if op.no_content:
        return None
    schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
    props  = schema.get("properties", {})
    if props and not schema.get("additionalProperties"):
        return go_name(op.op_id) + "Response"
    return "map[string]any"

def go_response_structs():
    parts = []
    for op in ops:
        rtype = go_response_type(op)
        if rtype in (None, "map[string]any"):
            continue
        schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {})
        props   = schema.get("properties", {})
        fields  = []
        for fname, fschema in sorted(props.items()):
            go_field = fname.capitalize()
            ftype = fschema.get("type", "any")
            add   = fschema.get("additionalProperties")
            if ftype == "string":
                go_type = "string"
            elif ftype == "boolean":
                go_type = "bool"
            elif ftype == "integer":
                go_type = "int64"
            elif ftype == "number":
                go_type = "float64"
            elif ftype == "array":
                go_type = "[]any"
            elif ftype == "object" and add:
                inner    = add.get("type", "any")
                inner_go = {"string": "string", "integer": "int64", "number": "float64", "boolean": "bool"}.get(inner, "any")
                go_type  = f"map[string]{inner_go}"
            else:
                go_type = "any"
            fields.append(f'\t{go_field} {go_type} `json:"{fname}"`')
        field_block = "\n".join(fields)
        parts.append(
            f"// {rtype} is returned by {go_name(op.op_id)}.\n"
            f"type {rtype} struct {{\n{field_block}\n}}"
        )
    return "\n\n".join(parts) + ("\n\n" if parts else "")

# Any integer query parameter across all ops needs strconv for formatting;
# only import it when actually used so unused-import can never fail the build.
_needs_strconv = any(ptype == "integer" for op in ops for _, ptype in op.query_params)

def go_method_signature_params(op):
    params = ["ctx context.Context"]
    if op.secure:
        params.append("apiKey string")
    for p in op.path_params:
        params.append(f"{param_arg_name(p)} string")
    if op.has_body:
        params.append("payload any")
    for name, ptype in op.query_params:
        go_type = "int64" if ptype == "integer" else "string"
        params.append(f"{param_arg_name(name)} {go_type}")
    return ", ".join(params)

def go_path_and_query_lines(op):
    """Returns (lines, path_expr): lines build any local vars needed to resolve
    the request path (path-param substitution and/or a query string), and
    path_expr is the Go expression to pass as the request path."""
    lines = []
    path_var = None
    if op.path_params:
        fmt_path = re.sub(r"\{(\w+)\}", "%s", op.path)
        args = ", ".join(f"url.PathEscape({param_arg_name(p)})" for p in op.path_params)
        lines.append(f'\tpath := fmt.Sprintf("{fmt_path}", {args})')
        path_var = "path"

    if op.query_params:
        lines.append("\tq := url.Values{}")
        for name, ptype in op.query_params:
            arg = param_arg_name(name)
            if ptype == "integer":
                lines.append(f"\tif {arg} != 0 {{")
                lines.append(f'\t\tq.Set("{name}", strconv.FormatInt({arg}, 10))')
                lines.append("\t}")
            else:
                lines.append(f'\tif {arg} != "" {{')
                lines.append(f'\t\tq.Set("{name}", {arg})')
                lines.append("\t}")
        if path_var is None:
            lines.append(f'\treqPath := "{op.path}"')
            path_var = "reqPath"
        lines.append("\tif len(q) > 0 {")
        lines.append(f'\t\t{path_var} += "?" + q.Encode()')
        lines.append("\t}")

    path_expr = path_var if path_var is not None else f'"{op.path}"'
    return lines, path_expr

def go_method(op):
    fn    = go_name(op.op_id)
    rtype = go_response_type(op)
    method_const = {
        "get": "http.MethodGet", "post": "http.MethodPost",
        "delete": "http.MethodDelete", "patch": "http.MethodPatch",
    }[op.method]

    param_str = go_method_signature_params(op)
    path_lines, path_expr = go_path_and_query_lines(op)

    if rtype is None:
        ret_sig = "error"
        nil_ret = ""
    elif rtype == "map[string]any":
        ret_sig = "(map[string]any, error)"
        nil_ret = "nil, "
    else:
        ret_sig = f"(*{rtype}, error)"
        nil_ret = "nil, "

    lines = [f"// {fn} calls {op.method.upper()} {op.path} ({op.op_id.replace('_', ' ')})."]
    if op.secure:
        lines.append(f"// apiKey is sent as the {api_key_header} header.")
    lines.append(f"func (c *Client) {fn}({param_str}) {ret_sig} {{")
    if op.has_body:
        lines += [
            "\tbody, err := json.Marshal(payload)",
            "\tif err != nil {",
            f'\t\treturn {nil_ret}fmt.Errorf("crucible: marshal payload: %w", err)',
            "\t}",
        ]
    lines += path_lines
    api_key_arg = "apiKey" if op.secure else '""'
    body_arg    = "body"   if op.has_body else "nil"
    lines += [
        f'\tresp, err := c.do(ctx, {method_const}, {path_expr}, {api_key_arg}, {body_arg})',
        "\tif err != nil {",
        f"\t\treturn {nil_ret}err",
        "\t}",
        "\tdefer resp.Body.Close()",
        "\tif err := checkError(resp); err != nil {",
        f"\t\treturn {nil_ret}err",
        "\t}",
    ]
    if rtype is None:
        lines.append("\treturn nil")
    elif rtype == "map[string]any":
        lines += [
            "\tout := make(map[string]any)",
            "\tif decErr := json.NewDecoder(resp.Body).Decode(&out); decErr != nil {",
            '\t\treturn nil, fmt.Errorf("crucible: decode response: %w", decErr)',
            "\t}",
            "\tif out == nil {",
            "\t\tout = make(map[string]any)",
            "\t}",
            "\treturn out, nil",
        ]
    else:
        lines += [
            f"\tvar out {rtype}",
            "\tif decErr := json.NewDecoder(resp.Body).Decode(&out); decErr != nil {",
            '\t\treturn nil, fmt.Errorf("crucible: decode response: %w", decErr)',
            "\t}",
            "\treturn &out, nil",
        ]
    lines.append("}")
    return "\n".join(lines)

structs_go = go_response_structs()
methods_go = "\n\n".join(go_method(op) for op in ops)

go_imports = ["bytes", "context", "encoding/json", "fmt", "io", "net/http", "net/url"]
if _needs_strconv:
    go_imports.append("strconv")
go_imports.append("strings")
go_imports_block = "\n".join(f'\t"{imp}"' for imp in go_imports)

write(os.path.join(GO_DIR, "client.go"), f"""\
{header}
// Package crucible is a type-safe Go client for the Crucible Gateway API.
// Construct a Client with New; all timeouts are the caller's responsibility.
package crucible

import (
{go_imports_block}
)

// Client calls the Crucible Gateway. Construct with New.
type Client struct {{
\tbaseURL *url.URL
\thttp    *http.Client
}}

// New returns a Client targeting baseURL, or an error if baseURL cannot be parsed.
// httpClient defaults to http.DefaultClient when nil. Note: http.DefaultClient has no
// timeout; callers must supply a client with Timeout set or use context.WithTimeout.
// baseURL is normalized: query strings, fragments, and credentials are stripped; trailing
// slashes on the path are removed so that appending spec paths (which start with "/") is correct.
// Any base path prefix (e.g. "/crucible" for a reverse-proxy subpath) is preserved.
func New(baseURL string, httpClient *http.Client) (*Client, error) {{
\tif httpClient == nil {{
\t\thttpClient = http.DefaultClient
\t}}
\tu, err := url.Parse(baseURL)
\tif err != nil {{
\t\treturn nil, fmt.Errorf("crucible: invalid baseURL %q: %w", baseURL, err)
\t}}
\tu.RawQuery = ""
\tu.Fragment = ""
\tu.User = nil
\tu.Path = strings.TrimRight(u.Path, "/")
\treturn &Client{{baseURL: u, http: httpClient}}, nil
}}

{structs_go}{methods_go}

func (c *Client) do(ctx context.Context, method, path, apiKey string, body []byte) (*http.Response, error) {{
\t// Append path directly: c.baseURL has no trailing slash; path starts with "/".
\t// This preserves any base path prefix (e.g. a reverse-proxy subpath).
\treqURL := c.baseURL.String() + path
\tvar req *http.Request
\tvar err error
\tif body != nil {{
\t\treq, err = http.NewRequestWithContext(ctx, method, reqURL, bytes.NewReader(body))
\t}} else {{
\t\treq, err = http.NewRequestWithContext(ctx, method, reqURL, nil)
\t}}
\tif err != nil {{
\t\treturn nil, fmt.Errorf("crucible: build request: %w", err)
\t}}
\tif body != nil {{
\t\treq.Header.Set("Content-Type", "application/json")
\t}}
\tif apiKey != "" {{
\t\treq.Header.Set("{api_key_header}", apiKey)
\t}}
\treq.Header.Set("Accept", "application/json")
\treturn c.http.Do(req)
}}

// checkError returns nil for 2xx responses without touching resp.Body.
// For non-2xx it reads the body (bounded to 64 KiB) to decode the gateway
// error envelope. The caller's defer resp.Body.Close() handles cleanup.
func checkError(resp *http.Response) error {{
\tif resp.StatusCode >= 200 && resp.StatusCode < 300 {{
\t\treturn nil
\t}}
\tvar envelope struct {{
\t\tError struct {{
\t\t\tCode      string `json:"code"`
\t\t\tMessage   string `json:"message"`
\t\t\tRetryable bool   `json:"retryable"`
\t\t}} `json:"error"`
\t}}
\t// Read up to 64 KiB + 1; the extra byte lets us detect oversized bodies explicitly.
\tconst limit = 64 << 10
\tbody, readErr := io.ReadAll(io.LimitReader(resp.Body, limit+1))
\tif readErr != nil {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (read body: %v)", resp.StatusCode, readErr)}}
\t}}
\tif len(body) > limit {{
\t\t// Best-effort bounded drain; connection may not be reusable if body exceeds ~68 KiB.
\t\t_, _ = io.CopyN(io.Discard, resp.Body, 4<<10)
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (error body >%d bytes)", resp.StatusCode, limit)}}
\t}}
\tif decErr := json.Unmarshal(body, &envelope); decErr != nil {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (invalid JSON: %v)", resp.StatusCode, decErr)}}
\t}}
\tif envelope.Error.Code == "" {{
\t\treturn &APIError{{Code: "UNKNOWN", Message: fmt.Sprintf("HTTP %d (missing error code)", resp.StatusCode)}}
\t}}
\treturn &APIError{{
\t\tCode:      envelope.Error.Code,
\t\tMessage:   envelope.Error.Message,
\t\tRetryable: envelope.Error.Retryable,
\t}}
}}
""")

# ── client_test.go ────────────────────────────────────────────────────────────

def go_test_call_args(op, with_key_literal=None):
    """Builds the call-site argument list in the same order as
    go_method_signature_params (minus ctx, which callers add explicitly)."""
    args = []
    if op.secure:
        args.append(f'"{with_key_literal}"' if with_key_literal else '"test-key"')
    for p in op.path_params:
        args.append(f'"test-{p}"')
    if op.has_body:
        args.append("map[string]any{\"x\": 1}")
    for name, ptype in op.query_params:
        args.append("0" if ptype == "integer" else '""')
    return args

def go_test_method(op):
    fn    = go_name(op.op_id)
    rtype = go_response_type(op)
    method_const = {
        "get": "http.MethodGet", "post": "http.MethodPost",
        "delete": "http.MethodDelete", "patch": "http.MethodPatch",
    }[op.method]

    test_path = op.path
    for p in op.path_params:
        test_path = test_path.replace("{" + p + "}", "test-" + p)

    schema = op.resp.get("content", {}).get("application/json", {}).get("schema", {}) if not op.no_content else {}
    props  = schema.get("properties", {})

    if rtype is None:
        resp_lines = ["\t\tw.WriteHeader(http.StatusNoContent)"]
    elif rtype == "map[string]any":
        resp_lines = ['\t\twriteJSON(w, map[string]any{"result": "ok"})']
    else:
        field_parts = []
        for fname in sorted(props.keys()):
            fschema = props[fname]
            ftype = fschema.get("type")
            if ftype == "string":
                field_parts.append(f'"{fname}": "ok"')
            elif ftype == "object":
                field_parts.append(f'"{fname}": map[string]any{{"db": "ok"}}')
            elif ftype == "integer" or ftype == "number":
                field_parts.append(f'"{fname}": 1')
            elif ftype == "boolean":
                field_parts.append(f'"{fname}": true')
            elif ftype == "array":
                field_parts.append(f'"{fname}": []any{{}}')
        resp_json = "map[string]any{" + ", ".join(field_parts) + "}"
        resp_lines = [f"\t\twriteJSON(w, {resp_json})"]

    call = f"c.{fn}(context.Background(), {', '.join(go_test_call_args(op))})" if go_test_call_args(op) else f"c.{fn}(context.Background())"

    lines = [
        f"func Test{fn}(t *testing.T) {{",
        f'\tc := newClient(t, func(w http.ResponseWriter, r *http.Request) {{',
        f'\t\tif r.Method != {method_const} || r.URL.Path != "{test_path}" {{',
        f'\t\t\tt.Errorf("unexpected request %s %s", r.Method, r.URL.Path)',
        "\t\t}",
    ]
    if op.secure:
        lines += [
            f'\t\tif got := r.Header.Get("{api_key_header}"); got == "" {{',
            f'\t\t\tt.Error("{api_key_header} header missing")',
            "\t\t}",
        ]
    if op.has_body:
        lines += [
            "\t\tvar reqBody map[string]any",
            "\t\tif err := json.NewDecoder(r.Body).Decode(&reqBody); err != nil {",
            '\t\t\tt.Fatalf("decode request body: %v", err)',
            "\t\t}",
            '\t\tif reqBody["x"] == nil {',
            '\t\t\tt.Error("expected request body to contain key x")',
            "\t\t}",
        ]
    lines += resp_lines
    lines += ["\t})"]

    if rtype is None:
        lines += [
            f"\terr := {call}",
            "\tif err != nil {",
            "\t\tt.Fatal(err)",
            "\t}",
            "}",
        ]
    elif rtype == "map[string]any":
        lines += [
            f"\tgot, err := {call}",
            "\tif err != nil {",
            "\t\tt.Fatal(err)",
            "\t}",
            "\tif got == nil {",
            '\t\tt.Fatal("expected non-nil map, got nil")',
            "\t}",
            '\tif got["result"] != "ok" {',
            '\t\tt.Errorf("result = %v, want \\"ok\\"", got["result"])',
            "\t}",
            "}",
        ]
    else:
        first_str_field = next(
            (fname for fname in sorted(props.keys()) if props[fname].get("type") == "string"),
            None
        )
        if first_str_field:
            go_field = first_str_field.capitalize()
            lines += [
                f"\tresp, err := {call}",
                "\tif err != nil {",
                "\t\tt.Fatal(err)",
