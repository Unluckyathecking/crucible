def py_path_and_query_lines(op):
    """Returns (lines, path_expr): lines build local vars needed to resolve the
    request path (path-param substitution and/or a query string); path_expr is
    the Python expression to pass as the request path."""
    lines = []
    path_var = None
    if op.path_params:
        tpl = op.path
        for p in op.path_params:
            tpl = tpl.replace("{" + p + "}", "{quote(str(" + py_arg_name(p) + "), safe='')}")
        lines.append('        path = f"' + tpl + '"')
        path_var = "path"

    if op.query_params:
        lines.append("        _params: Dict[str, str] = {}")
        for name, ptype in op.query_params:
            arg = py_arg_name(name)
            lines.append(f"        if {arg} is not None:")
            lines.append(f'            _params["{name}"] = str({arg})')
        if path_var is None:
            lines.append(f'        path = "{op.path}"')
            path_var = "path"
        lines.append("        if _params:")
        lines.append(f'            {path_var} += "?" + urlencode(_params)')

    path_expr = path_var if path_var is not None else f'"{op.path}"'
    return lines, path_expr

def py_method_signature_params(op):
    parts = ["self"]
    for p in op.path_params:
        parts.append(f"{py_arg_name(p)}: str")
    if op.has_body and op.body_required:
        parts.append("payload: Dict[str, Any]")
    elif op.has_body:
        # requestBody is optional in the spec: payload=None means "use
        # server-side defaults" (e.g. POST /v1/keys/{id}/rotate falls back to
        # a default grace period). It must NOT become an empty request body —
        # see the None-check emitted in py_method below.
        parts.append("payload: Optional[Dict[str, Any]] = None")
    for name, ptype in op.query_params:
        pytype = "int" if ptype == "integer" else "str"
        parts.append(f"{py_arg_name(name)}: Optional[{pytype}] = None")
    if op.secure:
        parts.append("api_key: Optional[str] = None")
    return ", ".join(parts)

def py_method(op):
    fn    = op.op_id
    rtype = py_response_type(op)
    method_upper = {"get": "GET", "post": "POST", "delete": "DELETE", "patch": "PATCH"}[op.method]
    sig = py_method_signature_params(op)

    lines = [f"    def {fn}({sig}) -> {rtype}:"]
    doc = f'{method_upper} {op.path} ({op.op_id.replace("_", " ")}).'
    lines.append(f'        """{doc}"""')

    body_expr = "None"
    if op.has_body and op.body_required:
        lines.append('        body = json.dumps(payload).encode("utf-8")')
        body_expr = "body"
    elif op.has_body:
        # Some optional-body handlers decode with a strict JSON decoder and
        # treat a fully empty body as an error (no special case for "no body
        # sent") — this always sends a valid JSON body, {} by default, never
        # omits it outright. Mirrors the Go/TS generators' identical guard.
        lines.append("        if payload is None:")
        lines.append("            payload = {}")
        lines.append('        body = json.dumps(payload).encode("utf-8")')
        body_expr = "body"

    path_lines, path_expr = py_path_and_query_lines(op)
    lines += path_lines

