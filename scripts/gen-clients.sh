class CaseInsensitiveHeaders(Dict[str, str]):
    """Plain dict subclass with case-insensitive .get()/__getitem__/__contains__.

    HTTP header names are case-insensitive (RFC 7230 §3.2); urllib.request
    normalizes the casing it sends on the wire (e.g. "X-API-Key" becomes
    "X-Api-Key"), so a plain dict keyed by the header name as received would
    make lookups depend on urllib's internal capitalization behavior.
    """

    def get(self, key: str, default: Any = None) -> Any:
        key_lower = key.lower()
        for k, v in self.items():
            if k.lower() == key_lower:
                return v
        return default


