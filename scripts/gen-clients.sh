import {{ ApiError }} from "./errors";

{interfaces}
export interface ClientOptions {{
  /** Override the global fetch implementation (useful in tests). Defaults to globalThis.fetch. */
  fetch?: typeof globalThis.fetch;
  /** Default API key sent as {api_key_header} on authenticated requests. */
  apiKey?: string;
}}

/**
 * Client is a type-safe consumer SDK for the Crucible Gateway.
 * It uses the runtime's fetch with no additional runtime dependencies.
 */
export class Client {{
  private readonly baseURL: string;
  private readonly fetchImpl: typeof globalThis.fetch;
  private readonly defaultApiKey: string | undefined;

  constructor(baseURL: string, options: ClientOptions = {{}}) {{
    // Normalize baseURL: strip query, fragment, and credentials (mirrors Go client).
    try {{
      const u = new URL(baseURL);
      u.search = "";
      u.hash = "";
      u.username = "";
      u.password = "";
      baseURL = u.toString();
    }} catch {{
      // Non-absolute URL — use as-is (e.g. relative path in tests).
    }}
    this.baseURL = baseURL.replace(/\\/+$/u, "");
    this.fetchImpl = options.fetch ?? globalThis.fetch;
    this.defaultApiKey = options.apiKey;
  }}

{methods_ts}

  private async get<T>(path: string, apiKey?: string): Promise<T> {{
    const headers: Record<string, string> = {{ Accept: "application/json" }};
    if (apiKey) {{
      headers["{api_key_header}"] = apiKey;
    }}
    const resp = await this.fetchImpl(this.baseURL + path, {{
      method: "GET",
      headers,
    }});
    if (!resp.ok) {{
      throw await ApiError.fromResponse(resp);
    }}
    return resp.json() as Promise<T>;
  }}

  private async post<T>(path: string, body: unknown, apiKey?: string): Promise<T> {{
    const headers: Record<string, string> = {{
      "Content-Type": "application/json",
      Accept: "application/json",
    }};
    if (apiKey) {{
      headers["{api_key_header}"] = apiKey;
    }}
    const resp = await this.fetchImpl(this.baseURL + path, {{
      method: "POST",
      headers,
      body: JSON.stringify(body),
    }});
    if (!resp.ok) {{
      throw await ApiError.fromResponse(resp);
    }}
    return resp.json() as Promise<T>;
  }}
}}
""")

